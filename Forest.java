package com.it.bean;

public class Forest {
  int aid;
  String aname;
  String apsd;
  
public Forest() {
	super();
}
public Forest(int aid, String aname, String apsd) {
	super();
	this.aid = aid;
	this.aname = aname;
	this.apsd = apsd;
}
public int getAid() {
	return aid;
}
public void setAid(int aid) {
	this.aid = aid;
}
public String getAname() {
	return aname;
}
public void setAname(String aname) {
	this.aname = aname;
}
public String getApsd() {
	return apsd;
}
public void setApsd(String apsd) {
	this.apsd = apsd;
}

  
}
